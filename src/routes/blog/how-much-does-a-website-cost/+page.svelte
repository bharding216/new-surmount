				<p>
					If you're a small business owner asking "how much does a website cost?", you're not alone.
					It's one of the most common questions we hear at Surmount Web Services. The truth is, website
					pricing can range from a few hundred dollars to well over $50,000 depending on what you need.
					That's a massive range, and it's easy to feel overwhelmed.
				</p>
